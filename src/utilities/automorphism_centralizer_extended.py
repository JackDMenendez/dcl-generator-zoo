import sympy as sp
from sympy import I, Matrix, eye, sqrt, simplify, Rational


def pauli_matrices():
    sx = Matrix([[0, 1], [1, 0]])
    sy = Matrix([[0, -I], [I, 0]])
    sz = Matrix([[1, 0], [0, -1]])
    return [sx, sy, sz]


def gell_mann_matrices():
    L1 = Matrix([[0, 1, 0], [1, 0, 0], [0, 0, 0]])
    L2 = Matrix([[0, -I, 0], [I, 0, 0], [0, 0, 0]])
    L3 = Matrix([[1, 0, 0], [0, -1, 0], [0, 0, 0]])
    L4 = Matrix([[0, 0, 1], [0, 0, 0], [1, 0, 0]])
    L5 = Matrix([[0, 0, -I], [0, 0, 0], [I, 0, 0]])
    L6 = Matrix([[0, 0, 0], [0, 0, 1], [0, 1, 0]])
    L7 = Matrix([[0, 0, 0], [0, 0, -I], [0, I, 0]])
    L8 = (1 / sqrt(3)) * Matrix([[1, 0, 0], [0, 1, 0], [0, 0, -2]])
    return [L1, L2, L3, L4, L5, L6, L7, L8]


def kron(*mats):
    r = mats[0]
    for M in mats[1:]:
        r = sp.kronecker_product(r, M)
    return r


def commutator(A, B):
    return simplify(A @ B - B @ A)


def is_zero_matrix(M):
    return all(simplify(e) == 0 for e in M)


def tensor_basis_su12():
    """Enumerate the 143 generators of su(12) as tensor products of
    Pauli matrices and Gell-Mann matrices.  Each basis element is
    labelled by its tensor signature (chirality_factor, isospin_factor,
    colour_factor) for later classification.

    Chirality factor: I_2 (label 'I') or sigma_a (labels 'X', 'Y', 'Z')
    Isospin factor:   I_2 (label 'I') or sigma_a (labels 'X', 'Y', 'Z')
    Colour factor:    I_3 (label 'I') or lambda_a (labels '1'..'8')

    Excludes the all-identity tensor (which is the trace, not in su(12))."""
    sx, sy, sz = pauli_matrices()
    L = gell_mann_matrices()

    chirality_options = [
        ('I', eye(2)),
        ('X', sx),
        ('Y', sy),
        ('Z', sz),
    ]
    isospin_options = [
        ('I', eye(2)),
        ('X', sx),
        ('Y', sy),
        ('Z', sz),
    ]
    colour_options = [('I', eye(3))] + [
        (str(a + 1), L[a]) for a in range(8)
    ]

    basis = []
    for (cl, C) in chirality_options:
        for (il, IS) in isospin_options:
            for (col_l, COL) in colour_options:
                if cl == 'I' and il == 'I' and col_l == 'I':
                    # Skip the identity (= trace, not in su(12))
                    continue
                basis.append({
                    'chir_label': cl,
                    'iso_label': il,
                    'col_label': col_l,
                    'matrix': kron(C, IS, COL),
                })
    return basis


def commutes_with_tick(elt, Y):
    """Check whether a basis element's matrix commutes with Y =
    sigma_x (x) I_2 (x) I_3."""
    c = commutator(elt['matrix'], Y)
    return is_zero_matrix(c)


def classify_element(elt):
    """Classify a centralizer element by its tensor signature."""
    cl = elt['chir_label']
    il = elt['iso_label']
    col_l = elt['col_label']

    is_iso_id = (il == 'I')
    is_col_id = (col_l == 'I')
    is_chir_id = (cl == 'I')
    is_chir_X = (cl == 'X')

    # Conjecture generators
    if is_chir_X and is_iso_id and is_col_id:
        return "J_1 (chirality rotation in conjecture's so(3,1))"
    if is_chir_id and not is_iso_id and is_col_id:
        return f"SU(2)_W generator T_{il}^W"
    if is_chir_id and is_iso_id and not is_col_id:
        return f"SU(3) Gell-Mann lambda_{col_l}"

    # Mixings (extras)
    if is_chir_id and not is_iso_id and not is_col_id:
        return f"isospin-colour mixing  sigma_{il} (x) lambda_{col_l}"
    if is_chir_X and not is_iso_id and is_col_id:
        return f"chirality_X x isospin  sigma_x x sigma_{il}"
    if is_chir_X and is_iso_id and not is_col_id:
        return f"chirality_X x colour  sigma_x x lambda_{col_l}"
    if is_chir_X and not is_iso_id and not is_col_id:
        return f"chirality_X x isospin-colour  sigma_x x sigma_{il} x lambda_{col_l}"

    return "unclassified (should not occur for centralizer elements)"


